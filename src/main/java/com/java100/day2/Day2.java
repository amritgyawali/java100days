package com.java100.day2;

//Write a program to declare different types of variables and print their values.
public class Day2 {
    static int a = 10;
    static String b = "Hello";
    static float c = 10.5f;
    public static void main(String[] args) {

        double d = 10.5;
        char e = 'a';
        boolean f = true;
        System.out.println("this is int"+a);
        System.out.println("this is string"+b);
        System.out.println("this is float"+c);
        System.out.println("this is double"+d);
        System.out.println("this is char"+e);
        System.out.println("this is boolean"+f);

    }
}
